subroutine get_ucenter()
implicit none
integer :: i,j

do j=1,njp
    do i=1,nip
        ucenter(i,j) = (u(i,j)+u(i-1,j))*0.5d0
        vcenter(i,j) = (v(i,j)+v(i,j-1))*0.5d0
    end do
end do

return
end subroutine get_ucenter
